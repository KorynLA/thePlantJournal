/***
Backend REST file to pull a plant entry in dynamoDB
Uses "get" call from dynamoDB
***/
import * as dynamoDbLib from "./libs/dynamo-libs";
import { success, failure } from "./libs/response-libs";

export async function main(event, context) {
  const params = {
    TableName: process.env.tableName,
    Key: {
      userId: event.requestContext.identity.cognitoIdentityId,
      plantId: event.pathParameters.id
    }
  };

  try {
    const result = await dynamoDbLib.call("get", params);
    if (result.Item) {
      // Return the retrieved item
      return success(result.Item);
    } else {
      //cant find item return
      return failure({ status: false, error: "Item not found." });
    }
  } catch (e) {
    console.log(e);
    return failure({ status: false });
  }
}